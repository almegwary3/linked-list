#include <iostream>
using namespace std;

#define SIZE 5
int queue[SIZE];
int front = -1;
int rear = -1;

bool isEmpty();
bool isFull();
void enqueue(int value);
void dequeue();
int peak();
void display();

int main()
{
    enqueue(10);
    enqueue(20);
    enqueue(30);
    enqueue(40);
    enqueue(50);
    display();
    dequeue();
    enqueue(60);
    display();
    cout << "Peak element: " << peak() << endl;
    return 0;
}

bool isEmpty()
{
    return (front == -1);
}

bool isFull()
{
    return ((rear + 1) % SIZE == front);
}

void enqueue(int value)
{
    if (isFull())
    {
        cout << "QUEUE IS FULL" << endl;
        return;
    }

    if (isEmpty())
    {
        front = rear = 0;
    }
    else
    {
        rear = (rear + 1) % SIZE;
    }

    queue[rear] = value;
}

void dequeue()
{
    if (isEmpty())
    {
        cout << "QUEUE IS EMPTY" << endl;
        return;
    }

    if (front == rear)
    {
        front = rear = -1;
    }
    else
    {
        front = (front + 1) % SIZE;
    }
}

int peak()
{
    if (isEmpty())
    {
        cout << "QUEUE IS EMPTY" << endl;
        return -1;
    }
    return queue[front];
}

void display()
{
    if (isEmpty())
    {
        cout << "QUEUE IS EMPTY" << endl;
        return;
    }

    cout << "Queue elements: ";
    int i = front;
    while (true)
    {
        cout << queue[i] << " ";
        if (i == rear)
            break;
        i = (i + 1) % SIZE;
    }
    cout << endl;
}
